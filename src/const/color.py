class COLOR(object):
    """Base class for color constants."""

    RESET: str = "\033[0m"
    BLACK: str = "\033[30m"
    RED: str = "\033[31m"
    GREEN: str = "\033[32m"
    ORANGE: str = "\033[33m"
    BLUE: str = "\033[34m"
    PURPLE: str = "\033[35m"
    CYAN: str = "\033[36m"
    LIGHT_GREY: str = "\033[37m"
    DARK_GREY: str = "\033[90m"
    LIGHT_RED: str = "\033[91m"
    LIGHT_GREEN: str = "\033[92m"
    YELLOW: str = "\033[93m"
    LIGHT_BLUE: str = "\033[94m"
    PINK: str = "\033[95m"
    LIGHT_CYAN: str = "\033[96m"
